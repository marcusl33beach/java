/**
 * Class to show a few operators
 */
public class Operators {
    public static void main(String[] args){
        int sum1 = 100 + 50; // Adding 100 and 50 to get 150
        int sum2 = sum1 + 250; // Adding sum1 to 250 to get 400
        int sum3 = sum2 + sum2; // Adding sum2 and sum2 to get 800
        System.out.println(sum3); // Prints out sum3
    }
} // returns 800
